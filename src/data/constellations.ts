export function matchConstellationByDate(month: number, day: number): ConstellationData {
  // Matches based on Solar Date
  if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) return CONSTELLATION_LIST[0]; // aries
  if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) return CONSTELLATION_LIST[1]; // taurus
  if ((month === 5 && day >= 21) || (month === 6 && day <= 21)) return CONSTELLATION_LIST[2]; // gemini
  if ((month === 6 && day >= 22) || (month === 7 && day <= 22)) return CONSTELLATION_LIST[3]; // cancer
  if ((month === 7 && day >= 23) || (month === 8 && day <= 22)) return CONSTELLATION_LIST[4]; // leo
  if ((month === 8 && day >= 23) || (month === 9 && day <= 22)) return CONSTELLATION_LIST[5]; // virgo
  if ((month === 9 && day >= 23) || (month === 10 && day <= 23)) return CONSTELLATION_LIST[6]; // libra
  if ((month === 10 && day >= 24) || (month === 11 && day <= 22)) return CONSTELLATION_LIST[7]; // scorpio
  if ((month === 11 && day >= 23) || (month === 12 && day <= 21)) return CONSTELLATION_LIST[8]; // sagittarius
  if ((month === 12 && day >= 22) || (month === 1 && day <= 19)) return CONSTELLATION_LIST[9]; // capricorn
  if ((month === 1 && day >= 20) || (month === 2 && day <= 18)) return CONSTELLATION_LIST[10]; // aquarius
  return CONSTELLATION_LIST[11]; // pisces (2.19 - 3.20)
}
